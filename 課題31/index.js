

var numA=10
var numB=20

if(numA===10 && numB===20){
    console.log("ANDの条件と一致した処理");
    console.log(numA===10 && numB===20);
}else{
    console.log("ANDの条件と一致しなかった処理");
    console.log(numA===10 && numB===20);
}

if(numA===10 || numB===10){
    console.log("ANDの条件と一致した処理");
    console.log(numA===10 || numB===20);
}else{
    console.log("ANDの条件と一致しなかった処理");
    console.log(numA===10 || numB===20);
}

if(!numA){
    console.log("ANDの条件と一致した処理");
    console.log(!numA);
}else{
    console.log("ANDの条件と一致しなかった処理")
    console.log(!numA);
}


